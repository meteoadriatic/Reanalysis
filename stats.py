
def variation(mylist):
    from scipy.stats import variation
    return variation(mylist)

def gmean(mylist):
    from scipy.stats import gmean
    return gmean(mylist)

def hmean(mylist):
    from scipy.stats import hmean
    return hmean(mylist)

def kurtosis(mylist):
    from scipy.stats import kurtosis
    return kurtosis(mylist)

def skew(mylist):
    from scipy.stats import skew
    return skew(mylist)
